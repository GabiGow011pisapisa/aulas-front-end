// Criação do array animais com quatro nomes de animais
let animais = ['Leão', 'Elefante', 'Tigre', 'Girafa'];

// Exibe o segundo animal no console
console.log('Segundo animal:', animais[1]);

// Adiciona um novo animal ao final do array
animais.push('Zebra');
console.log('Array atualizado com o novo animal:', animais);

// Remove o primeiro animal do array
animais.shift();
console.log('Array atualizado após remover o primeiro animal:', animais);

// Inverte a ordem dos elementos do array
animais.reverse();
console.log('Array invertido:', animais);
